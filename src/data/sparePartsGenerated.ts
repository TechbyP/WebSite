const normalizeAssemblies = (
  catalog: (typeof RAW_GENERATED_SPARE_PARTS)[number]
) => {
  if (catalog.id !== 'mp-shared') {
    return catalog.assemblies;
  }

  const towerPartOne = catalog.assemblies.find((assembly) => assembly.id === 'mp_page_04');
  const towerPartTwo = catalog.assemblies.find((assembly) => assembly.id === 'mp_page_05');

  return catalog.assemblies
    .filter((assembly) => assembly.id !== 'mp_page_05')
    .map((assembly) => {
      if (assembly.id === 'mp_page_04' && towerPartOne) {
        return {
          ...towerPartOne,
          title: 'Turm',
          imageAsset: 'mp/mp-page-04.png',
          items: towerPartTwo
            ? [...towerPartOne.items, ...towerPartTwo.items]
            : towerPartOne.items,
        };
      }

      if (assembly.id === 'mp_page_11') {
        return {
          ...assembly,
          imageAsset: 'boprob/boprob.png',
        };
      }

      return assembly;
    });
};

export const GENERATED_SPARE_PARTS_CATALOGS: SparePartsCatalog[] = RAW_GENERATED_SPARE_PARTS.map((catalog) => ({
  ...catalog,
  assemblies: normalizeAssemblies(catalog).map((assembly) => ({
    ...assembly,
    imageUrl: new URL(`../assets/Technical/generated/spare-parts/${assembly.imageAsset}`, import.meta.url).href,
  })),
}));
